# 反向索引堆实现 Dijkstra（单源最短路径）
# 适用于边权非负图

# 核心结构：
# 1. 小根堆（按当前最短距离排序）
# 2. where数组：记录节点在堆中的位置

# where[v] 含义：
# -1 ：从未进过堆
# >=0：在堆中（记录堆下标）
# -2 ：已经弹出（最短距离已确定）

# 两个核心思想：
# 1. 一旦节点从堆中弹出，最短距离就确定
# 2. 只更新“还在堆中的节点”或“从未进堆的节点”

# 处理边 u -> v (权重 w) 时：
# 1. v 从未进堆 → 加入堆
# 2. v 已弹出 → 忽略
# 3. v 在堆中 → 如果更小就更新距离并调整堆

# 本质：
# 反向索引堆 = 可修改的小根堆
# 支持 O(logN) 原地更新距离

# 时间复杂度：
# O((N + M) log N)
